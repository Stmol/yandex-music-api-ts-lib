const declarationCompilerOptions: ts.CompilerOptions = {
  module: ts.ModuleKind.ESNext,
  moduleResolution: ts.ModuleResolutionKind.Bundler,
  skipLibCheck: true,
  target: ts.ScriptTarget.ESNext,
};

export function combineExpectedExportNames(...groups: ReadonlyArray<ReadonlyArray<string>>): string[] {
  return [...new Set(groups.flat())].sort((left, right) => left.localeCompare(right));
}

export async function readRuntimeExportNames(rootDir: string, modulePath: string): Promise<string[]> {
  const module: unknown = await import(pathToFileURL(path.join(rootDir, modulePath)).href);

  assert.equal(typeof module, "object");
  assert.notEqual(module, null);

  return Object.keys(module as Record<string, unknown>).sort((left, right) => left.localeCompare(right));
}

export function readDeclarationExportNames(rootDir: string, declarationPath: string): string[] {
  const absolutePath = path.join(rootDir, declarationPath);
  const program = ts.createProgram([absolutePath], declarationCompilerOptions);
  const sourceFile = program.getSourceFile(absolutePath);

  assert.ok(sourceFile, `Declaration source file is missing: ${declarationPath}`);

  const checker = program.getTypeChecker();
  const moduleSymbol = checker.getSymbolAtLocation(sourceFile);

  assert.ok(moduleSymbol, `Could not resolve module symbol for ${declarationPath}`);

  return checker
    .getExportsOfModule(moduleSymbol)
    .map((symbol) => symbol.getName())
    .sort((left, right) => left.localeCompare(right));
}
